/**
 * Theme system — Dark / Light × Default / High-Contrast
 *
 * createTheme() builds the closure-based properties (elemFill, elemStroke,
 * elemNum, grid) from a flat color token object, eliminating the duplication
 * that previously existed across 4 hand-written theme blocks.
 */

function createTheme(t) {
  return {
    ...t,
    gridDash: (i) => i % 2 ? "2,5" : "none",
    grid:      (i) => i % 2 ? t._gridOdd : t._gridEven,
    elemNum:   (e) => e.apd ? t._elemNumApd : t._elemNumStd,
    elemFill:  (e, on) => {
      if (on) return e.apd ? t._fillOnApd : t._fillOnStd;
      if (e.apd === "patent")   return t._fillPatent;
      if (e.apd === "inferred") return t._fillInfer;
      if (e.nd > 1.78)          return t._fillHighNd;
      if (e.nd > 1.6)           return t._fillMidNd;
      return t._fillLowNd;
    },
    elemStroke: (e, on) => {
      if (on)                    return t._strokeOn;
      if (e.apd === "patent")   return t._strokePatent;
      if (e.apd === "inferred") return t._strokeInfer;
      return t._strokeDefault;
    },
  };
}

