function entryByName(name: string): GlassEntry {
  const entry = GLASS_CATALOG_ENTRIES_BY_NAME.get(name);
  if (!entry) throw new Error(`Glass catalog source-order entry ${name} has no vendor shard entry.`);
  return entry;
}

