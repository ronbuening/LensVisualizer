/** Build a single-pass hex→hex lookup for a palette so replacements never cascade. */
function buildLookup(palette) {
  const map = new Map();
  for (const [group, literals] of Object.entries(GROUPS)) {
    for (const lit of literals) map.set(lit.toLowerCase(), palette[group]);
  }
  const pattern = new RegExp(Array.from(map.keys()).join("|"), "gi");
  return { map, pattern };
}

function recolor(svg, palette, id) {
  const { map, pattern } = buildLookup(palette);
  const body = svg.replace(pattern, (hit) => map.get(hit.toLowerCase()) ?? hit);
  return body.replace(
    "<svg ",
    `<!-- Generated by scripts/generate-holiday-branding.mjs — holiday: ${id}. Do not edit by hand. -->\n<svg `,
  );
}

const baseMark = readFileSync(resolve(BRANDING, "mark-card.svg"), "utf8");
const baseFavicon = readFileSync(resolve(BRANDING, "favicon-dark.svg"), "utf8");

mkdirSync(OUT_DIR, { recursive: true });

let count = 0;
for (const [id, palette] of Object.entries(PALETTES)) {
  writeFileSync(resolve(OUT_DIR, `mark-${id}.svg`), recolor(baseMark, palette, id));
  writeFileSync(resolve(OUT_DIR, `favicon-${id}.svg`), recolor(baseFavicon, palette, id));
  count += 2;
}

console.log(`Generated ${count} holiday brand assets for ${Object.keys(PALETTES).length} holidays in ${OUT_DIR}`);
